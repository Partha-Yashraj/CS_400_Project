/**
 * This class holds the city-cost pair
 * 
 * @author Partha Yashraj
 *
 */
public class CityCostPair {
  int cost, city; // the cost from/to the city, city

  /**
   * Constructor to initialize the variables
   * 
   * @param city the city
   * @param cost the cost from/to the city
   */
  CityCostPair(int city, int cost) {
    this.city = city;
    this.cost = cost;
  }

}
